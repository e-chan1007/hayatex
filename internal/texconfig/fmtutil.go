package texconfig

import (
	"bytes"
	"context"
	"fmt"
	"os"
	"path/filepath"
	"runtime"
	"strings"

	"github.com/e-chan1007/hayatex/internal/config"
	"github.com/e-chan1007/hayatex/internal/resolver"
	"github.com/e-chan1007/hayatex/internal/utils"
	"golang.org/x/sync/errgroup"
)

type FormatJob struct {
	Name     string
	Engine   string
	Patterns string
	Options  string
}

func (j *FormatJob) ToString() string {
	return fmt.Sprintf("%s %s %s %s", j.Name, j.Engine, j.Patterns, j.Options)
}

func ExecuteFormatCommands(ctx context.Context, config *config.Config, deps *resolver.TLPackageList, ce *utils.CommandExecutor) error {
	jobs := extractFormatJobs(deps)
	generateFmtutilConfig(config.TexDir, jobs)
	configPath, _ := generateFmtutilConfig(config.TexDir, jobs)
	if config.CompatMode {
		return runFmtutilCommand(ctx, config, ce, configPath)
	}
	err := runFmtutilCommands(ctx, config, ce, jobs)
	if err != nil {
		return fmt.Errorf("Failed to execute format commands: %v", err)
	}
	return nil
}

func extractFormatJobs(deps *resolver.TLPackageList) []*FormatJob {
	var jobs []*FormatJob
	for _, pkg := range *deps {
		for _, execLine := range pkg.Executes {
			if ok, rest := utils.HasPrefixIgnoreCase(execLine, "AddFormat "); ok {
				formatJob := parseAddFormat(rest)
				if formatJob != nil {
					jobs = append(jobs, formatJob)
				}
			}
		}
	}
	return jobs
}

func generateFmtutilConfig(texdir string, jobs []*FormatJob) (string, error) {
	fmtutilDir := filepath.Join(texdir, "texmf-config", "web2c")
	if err := os.MkdirAll(fmtutilDir, 0755); err != nil {
		return "", err
	}

	fmtutilConfigPath := filepath.Join(fmtutilDir, "fmtutil.cnf")
	f, err := os.Create(fmtutilConfigPath)
	if err != nil {
		return "", err
	}
	defer f.Close()

	for _, job := range jobs {
		fmt.Fprintln(f, job.ToString())
	}

	f.WriteString("# Generated by HayaTeX\n")

	return fmtutilConfigPath, nil
}

func parseAddFormat(formatLine string) *FormatJob {
	params := utils.ParseKeyValuePairs(formatLine)

	if params["mode"] == "disabled" {
		return nil
	}

	job := &FormatJob{
		Name:     params["name"],
		Engine:   params["engine"],
		Patterns: params["patterns"],
		Options:  params["options"],
	}
	if job.Patterns == "" {
		job.Patterns = "-"
	}

	if job.Name == "" || job.Engine == "" {
		return nil
	}

	return job
}

func runFmtutilCommand(ctx context.Context, config *config.Config, ce *utils.CommandExecutor, configPath string) error {
	cmd := ce.NewCommandContext(ctx, "fmtutil-sys", fmt.Sprintf("--cnffile=%s", configPath), "--all", "--nohash")
	cmd.Dir = config.TexDir
	return cmd.Run()
}

func runFmtutilCommands(ctx context.Context, config *config.Config, ce *utils.CommandExecutor, jobs []*FormatJob) error {
	eg, ctx := errgroup.WithContext(ctx)
	semaphore := make(chan struct{}, runtime.NumCPU())

	for _, job := range jobs {
		eg.Go(func() error {
			semaphore <- struct{}{}
			defer func() { <-semaphore }()
			destDir := filepath.Join(config.TexDir, "texmf-var", "web2c", job.Engine)
			if err := os.MkdirAll(destDir, 0755); err != nil {
				return err
			}
			args := []string{
				"-ini",
				"-jobname=" + job.Name,
				"-progname=" + job.Name,
			}
			if (job.Engine == "uptex" || job.Engine == "euptex") && (strings.HasPrefix(job.Name, "ep") || strings.HasPrefix(job.Name, "p")) {
				args = append(args, "-kanji-internal=sjis")
			}
			if job.Options != "" {
				args = append(args, strings.Fields(job.Options)...)
			}
			var outputBuffer bytes.Buffer
			fmt.Fprintf(&outputBuffer, "Executing format command for %s: %s %s\n", job.Name, job.Engine, strings.Join(args, " "))
			cmd := ce.NewCommandContext(ctx, job.Engine, args...)
			stdout := cmd.Stdout
			cmd.Dir = destDir
			cmd.Stdout = &outputBuffer
			cmd.Stderr = &outputBuffer
			err := cmd.Run()
			if err != nil {
				fmt.Fprintf(&outputBuffer, "Error executing format command for %s: %v\n", job.Name, err)
				stdout.Write(outputBuffer.Bytes())
				return fmt.Errorf("Failed to run command for format %s: %v", job.Name, err)
			}
			fmt.Fprintf(&outputBuffer, "Format %s generated successfully.\n\n", job.Name)
			stdout.Write(outputBuffer.Bytes())
			return nil
		})
	}
	return eg.Wait()
}
